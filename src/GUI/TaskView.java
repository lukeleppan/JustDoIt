    private void refreshSubtaskList() {
        SubtaskManager subtaskManager = new SubtaskManager();
        List<Subtask> subtasks = subtaskManager.GetTaskSubtasks(task);

        for (int i = 0; i < pnlSubtaskList.getComponentCount(); i++) {
            if (i > 0) {
                pnlSubtaskList.remove(i);
            }
        }
        pnlSubtaskList.invalidate();
        pnlSubtaskList.validate();
        pnlSubtaskList.repaint();
        Dimension preferredSizeTODO = new Dimension(250, (70 * subtasks.size()) + 50);
    }

